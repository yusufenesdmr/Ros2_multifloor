#!/usr/bin/env python3
"""
wormhole_navigator.py  –  ROS 2 Humble
=======================================
Multi-map (multi-floor) navigation using wormhole logic ported from the
original ROS 1 `multi_map_nav` package (Multi-Map-Navigation repo).

Key differences from the ROS 1 original
-----------------------------------------
  • rclpy instead of rospy
  • Nav2 NavigateToPose action instead of move_base
  • /map_server/load_map service (Nav2 Lifecycle Map Server)
    instead of `rosrun map_server map_server` + system()
  • rclpy_action instead of actionlib
  • SQLite3 wormhole DB schema kept identical for compatibility

Elevator door virtual wall logic  (NEW – per user requirements)
---------------------------------------------------------------
Before the robot approaches an elevator:
  1. A virtual LINE barrier is drawn across the elevator door opening
     via nav2_virtual_layer AddLine service → robot cannot enter yet.
  2. The robot waits for /elevator_status to confirm the car has arrived
     and doors have opened.
  3. Only then is the virtual wall removed (RemoveShape) and the robot
     drives into the elevator.
  4. After the elevator reports arrival at the target floor, the map is
     hot-swapped and AMCL re-initialised at the exit position.

SDF2MAP compatibility
---------------------
Maps generated by SDF2MAP (https://github.com/sherif1152/SDF2MAP) use:
  - resolution: 0.05 m/px
  - origin: [x, y, 0.0]  (bottom-left in world coords)
  - occupied_thresh: 0.65 / free_thresh: 0.25
The wormhole DB stores the full path to each floor's generated YAML file.

Database schema (same as original repo)
----------------------------------------
  CREATE TABLE wormholes (
      id              INTEGER PRIMARY KEY AUTOINCREMENT,
      from_map        TEXT NOT NULL,
      to_map          TEXT NOT NULL,
      from_x          REAL NOT NULL,
      from_y          REAL NOT NULL,
      from_yaw        REAL NOT NULL DEFAULT 0.0,
      map_yaml_path   TEXT NOT NULL,
      -- Elevator door endpoints for virtual wall:
      door_x1         REAL NOT NULL DEFAULT 0.0,
      door_y1         REAL NOT NULL DEFAULT 0.0,
      door_x2         REAL NOT NULL DEFAULT 0.0,
      door_y2         REAL NOT NULL DEFAULT 0.0,
      elevator_id     TEXT NOT NULL DEFAULT 'elevator_A'
  );

Usage
-----
  ros2 run hotel_luggage_robot wormhole_navigator.py \\
      --ros-args \\
      -p wormhole_db_path:=/path/to/hotel_wormholes.db \\
      -p initial_map:=floor_1 \\
      -p maps_base_dir:=/path/to/maps/

  # Seed demo database:
  python3 wormhole_navigator.py --seed-db /path/to/hotel.db /path/to/maps/
"""

