    def __init__(self):
        super().__init__("wormhole_navigator")

        # ── Parameters ────────────────────────────────────────────────────────
        self.declare_parameter("wormhole_db_path",   "hotel_wormholes.db")
        self.declare_parameter("initial_map",        "floor_1")
        self.declare_parameter("maps_base_dir",      "")
        self.declare_parameter("map_frame",          "map")
        self.declare_parameter("robot_frame",        "base_footprint")
        self.declare_parameter("nav2_action_name",
            "/navigate_to_pose")
        self.declare_parameter("load_map_service",
            "/map_server/load_map")
        self.declare_parameter("initial_pose_topic",
            "/initialpose")
        self.declare_parameter("elevator_status_topic",
            "/elevator_controller/door_state")
        self.declare_parameter("virtual_layer_ns",
            "/global_costmap/virtual_layer")
        self.declare_parameter("map_load_wait_sec",  2.5)
        self.declare_parameter("elevator_wait_timeout_sec", 60.0)
        self.declare_parameter("door_wall_thickness_m",     0.15)
        self.declare_parameter("door_wall_cost",            254)
        self.declare_parameter("goal_yaw_tolerance", 0.2)
        self.declare_parameter("wormhole_approach_tolerance", 0.25)

        # ── State ─────────────────────────────────────────────────────────────
        self._current_map: str = self.get_parameter("initial_map").value
        self._db_path: str     = self.get_parameter("wormhole_db_path").value
